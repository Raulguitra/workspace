/*10) Muestra los números del 1 al 100 (ambos incluidos). Usa un bucle while.*/


public class Ejercicio10 {
 
    public static void main(String[] args) {
 
        int num=1;
 
        //Definimos el bucle, incluye el 100
        while (num<=100){
            System.out.println(num);
            //Incrementamos num
            num++;
        }
    }
}